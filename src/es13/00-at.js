//at 
// sirve para retornar el caracter de un string en un indice especifico
// se puede usar numeros negativos para empezar desde el final

const array = ["one", "two", "three", "four", "five"];

console.log(array.at(array.length - 1)); // devuelve el ultimo elemento

console.log(array.at(-1)); // devuelve el ultimo elemento